echo ""; echo "== разнообразие речи Сдвига =="
python3 - << 'STORY_EOF'
# Разнообразим речь Сдвига — убираем однотипный "малыш", даём живой характер
import json

# ─── LEVEL 1-1 ───
p1="src/main/resources/static/scenarios/level-1-1.json"
d=json.load(open(p1,encoding='utf-8'))
E=d['events']

# s1: убираем малыш — резче, без обращения
E['s1']['dialogue']='Рекрут: «Опять ничего не взяли?»\nСдвиг: «Ничего. Шесть витрин, шесть дыр, ноль краж. За такое платят. Вопрос — кто и за что».'

# s2: вместо малыш — горькая ирония, обращение по делу
E['s2']['dialogue']='Сдвиг: «За красоту платят только двое. Сумасшедшие — и те, кто считает себя художником. Чувствуешь разницу?»\nРекрут: «Не очень».\nСдвиг: «Я тоже. Пока. Но это не вандализм — это почерк. Идём искать руку, что его оставила».'

# s5: тень — напряжённо, без малыш, командным шёпотом
E['s5']['dialogue']='Сдвиг: «Не оборачивайся резко. Через дорогу, в арке».\nРекрут: «Вижу. Давно он там?»\nСдвиг: «Дольше нас. Запомни это чувство, парень. С ним тебе теперь жить».'

# end: финал — тяжело, по-наставнически, без малыш
E['end']['dialogue']='Сдвиг: «Кто-то готовит сцену. И выбрал именно этот квартал. Этот музей. Чует моё нутро — мы сюда ещё вернёмся».'

E['s5']['dialogue']='Рекрут: «Вижу. Давно он там?»\nСдвиг: «Дольше нас. Запомни это чувство, {парень|девочка}. С ним тебе теперь жить».'
# поправим s5 целиком (выше уже задан, перезапишем с гендером)
E['s5']['dialogue']='Сдвиг: «Не оборачивайся резко. Через дорогу, в арке».\nРекрут: «Вижу. Давно он там?»\nСдвиг: «Дольше нас. Запомни это чувство, {парень|девочка}. С ним тебе теперь жить».'

# react в s4: разнообразим
E['s4']['react']='Сдвиг: «Новенькие. Из одной пачки. Так платят, когда не хотят, чтобы запомнили лицо».\nРекрут: «Богатый наниматель для битья стёкол».\nСдвиг: «Богатый и с фантазией. Худшее, что бывает».'

# концовка win — убираем "малыш" (там был после фикса китайского)
d['endings']['win']['text']='Ты понял главное: это не вандализм, а репетиция. Кто-то проверяет квартал перед большим ходом — и ход будет здесь, у музея. Сдвиг глянул на тёмную громаду в конце улицы: «Когда начнётся по-настоящему — ты уже будешь знать, где смотреть». Дождь стих. Музей молчал — пока.'

json.dump(d,open(p1,'w',encoding='utf-8'),ensure_ascii=False,indent=2)
print("✓ level-1-1: речь Сдвига разнообразена")

# ─── CASE 001 (музей) ───
p2="src/main/resources/static/scenarios/case001.json"
d2=json.load(open(p2,encoding='utf-8'))
E2=d2['events']

# L1_c2: убираем малыш — суше
E2['L1_c2']['dialogue']='Сдвиг: «Веришь в проклятия? Городской департамент верит. Боятся войти в музей».\nРекрут: «Я верю в улики. Призраки их не оставляют».\nСдвиг: «Потому ты и сидишь сейчас рядом со мной, а не они».'

# L1_c5: запах — оставляем обращение, но другое (вместо малыш — "примечай")
E2['L1_c5']['dialogue']='Сдвиг: «Чувствуешь? Гроза за закрытой дверью. Примечай этот запах. Он ещё вернётся к нам».'

# eL2a react: вместо "Видишь, малыш?" — иначе
E2['eL2a']['react']='Сдвиг: «Масло и след волочения. Никакой мистики — голая механика».\nРекрут: «Значит, в зале было что-то тяжёлое. И его убрали».'

# eL2c2 react: вместо "Не пустота, малыш"
E2['eL2c2']['react']='Рекрут: «Сквозняк из глухой стены. За ней пустота».\nСдвиг: «Не пустота. Ход. Кто-то прорезал в музее чёрную дверь».'

# eShift1: вместо "Ну, малыш, картина сложилась" 
E2['eShift1']['dialogue']='Сдвиг: «Ну вот, картина и сложилась. Теперь скажи мне сам — что здесь произошло?»'

json.dump(d2,open(p2,'w',encoding='utf-8'),ensure_ascii=False,indent=2)
print("✓ case001: речь Сдвига разнообразена")

# Проверка: сколько "малыш" осталось
import re
total=0
for f in [p1,p2]:
    dd=json.load(open(f,encoding='utf-8'))
    cnt=0
    for e in dd['events'].values():
        for fld in ['text','dialogue','intro','react']:
            cnt+=len(re.findall(r'малыш',str(e.get(fld,'')),re.I))
    for ek in dd.get('endings',{}).values():
        cnt+=len(re.findall(r'малыш',str(ek.get('text','')),re.I))
    print(f"  {f.split('/')[-1]}: осталось 'малыш' = {cnt}")
    total+=cnt
print(f"ИТОГО малыш: {total} (было 9)")

STORY_EOF
echo ""
